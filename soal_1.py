# Memasukkan nilai A dan B
A = float(input("Masukkan angka A: "))
B = float(input("Masukkan angka B: "))

# Menghitung hasil operasi
penjumlahan = A + B
pengurangan = A - B
perkalian = A * B

# Menampilkan hasil
print(f"Hasil penjumlahan {A} + {B} = {penjumlahan}")
print(f"Hasil pengurangan {A} - {B} = {pengurangan}")
print(f"Hasil perkalian {A} * {B} = {perkalian}")